package com.pratice;

public class IfElseProgram {
    public static void main(String[] args) {

        int marks = 20;

        if (marks >= 25) {
            System.out.println("Pass");
        } else {
            System.out.println("Fail");
        }
        if (marks >= 15) {
            System.out.println("Pass");
        } else {
            System.out.println("Fail");
        }
        if (marks >= 35) {
            System.out.println("Pass");
        } else {
            System.out.println("Fail");
        }

    }
}
